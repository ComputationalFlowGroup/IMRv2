% brief This script generates the golden resolution data for IMR
clc;
clear;
close;

format long;
    
    addpath('../../src/forward_solver/');
    
    % material parameter test cases
    tvector = linspace(0,100E-6,1000);
    collapse = 1;
    masstrans = 1;
    vapor = 1;
    count = 1;
    bubtherm = 1;
    medtherm = 1;
    R0 = 200e-6;
    Req = R0/8;
    mu = 1E-1;
    G = 1E3;
    alphax = 0.5;
    lambda1 = 1E-3;
    lambda2 = 0;
    radial = 3;
    stress = 3;
    
    Nresolved = 1024;
    
    % exact solution
    varin = {'progdisplay',1,...
        'radial',radial,...
        'bubtherm',bubtherm,...
        'tvector',tvector,...
        'vapor',vapor,...
        'medtherm',medtherm,...
        'masstrans',masstrans,...
        'collapse',collapse,...
        'lambda2',lambda2,...
        'Req',Req,...
        'R0',R0,...
        'mu',mu,...
        'G',G,...
        'alphax',alphax,...
        'lambda1',lambda1,...
        'stress',stress,...
        'Nt',Nresolved,...
        'Mt',Nresolved};
    
    [tfd1024,Rfd1024] = f_imr_fd(varin{:});
    save("fd1024.mat","tfd1024","Rfd1024");
